package model.model;

import model.db.CuentaDB;

/**
 *
 * @author b4st4rd
 */
public class Cliente {
    protected String id;
    protected String contraseña;
    protected String nombre;
    protected String telefono;
    protected String direccion;
    protected String ocupacion;
    protected boolean suscrito;

    public String añadirCuenta( boolean type){
        return CuentaDB.addCuenta(id, type);
    }
    
    
    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the contraseña
     */
    public String getContraseña() {
        return contraseña;
    }

    /**
     * @param contraseña the contraseña to set
     */
    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the ocupacion
     */
    public String getOcupacion() {
        return ocupacion;
    }

    /**
     * @param ocupacion the ocupacion to set
     */
    public void setOcupacion(String ocupacion) {
        this.ocupacion = ocupacion;
    }

    /**
     * @return the suscrito
     */
    public boolean isSuscrito() {
        return suscrito;
    }

    /**
     * @param suscrito the suscrito to set
     */
    public void setSuscrito(boolean suscrito) {
        this.suscrito = suscrito;
    }
    
}
